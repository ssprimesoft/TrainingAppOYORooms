package com.sagar.oyoroomstest.model;

public class SliderImageListData {

    private String id;
    private String img;

    public SliderImageListData(String id, String img) {
        this.id = id;
        this.img = img;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }
}
